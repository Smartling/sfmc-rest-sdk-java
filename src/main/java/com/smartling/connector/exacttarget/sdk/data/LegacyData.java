package com.smartling.connector.exacttarget.sdk.data;

public class LegacyData
{
    private String legacyId;
    private String legacyKey;
    private String legacyType;
    private String legacyCategoryId;

    public String getLegacyId()
    {
        return legacyId;
    }

    public void setLegacyId(final String legacyId)
    {
        this.legacyId = legacyId;
    }

    public String getLegacyKey()
    {
        return legacyKey;
    }

    public void setLegacyKey(final String legacyKey)
    {
        this.legacyKey = legacyKey;
    }

    public String getLegacyType()
    {
        return legacyType;
    }

    public void setLegacyType(final String legacyType)
    {
        this.legacyType = legacyType;
    }

    public String getLegacyCategoryId()
    {
        return legacyCategoryId;
    }

    public void setLegacyCategoryId(final String legacyCategoryId)
    {
        this.legacyCategoryId = legacyCategoryId;
    }
}
